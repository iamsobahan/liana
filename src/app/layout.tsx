import type { Metadata } from "next";
import "./globals.css";

import { Rajdhani, Raleway } from "next/font/google";
import Header from "@/components/header";
import FooterPage from "@/components/Footer/page";
import MobileBottomNav from "@/components/Tab/page";
import ReduxProvider from "@/redux/provider";
import ToasterProvider from "@/toast";
import { getAllCategories } from "@/lib/data/category";

const rajdhani = Rajdhani({
  subsets: ["latin"],
  weight: ["400", "700"],
  display: "swap",
  variable: "--font-rajdhani",
});

const raleway = Raleway({
  subsets: ["latin"],
  variable: "--font-raleway",
});

export const metadata: Metadata = {
  title: "Eliana",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const allCategories = await getAllCategories();
  return (
    <html lang="en">
      <body
        className={`${rajdhani.variable} ${raleway.variable} bg-white scroll-smooth`}
        cz-shortcut-listen="true"
      >
        <main>
          <ReduxProvider>
            <Header categories={allCategories.data} />
            <MobileBottomNav />
            {children}
            <FooterPage />
          </ReduxProvider>
          <ToasterProvider />
        </main>
      </body>
    </html>
  );
}
